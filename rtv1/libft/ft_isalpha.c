#include "libft.h"

int		ft_isalpha(int c)
{
	unsigned char ch;

	ch = c;
	if (c >= 0 && c <= 255 && ((ch >= 'a' && ch <= 'z')
				|| (ch >= 'A' && ch <= 'Z')))
		return (1);
	return (0);
}
